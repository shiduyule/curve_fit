import numpy as np

# 已知的初始点和结束点
x1 = 0
y1 = 1
x2 = 15
y2 = 0.2

# 求衰减系数 b
# 我们已知公式为 f(x) = a * e^(-bx)
# 根据已知的两点 (x1, y1) 和 (x2, y2) ，我们可以得到两个方程：
# y1 = a * e^(-b * x1)
# y2 = a * e^(-b * x2)
# 将上述两个方程联立解出 b
b = np.log(y2 / y1) / (x1 - x2)

# 计算初始值 a
# 由于已知 f(0) = 1，我们可以得到：1 = a * e^(-b * 0)，即 a = y1 / e^(-b * x1)
a = y1 / np.exp(-b * x1)

# 打印结果
print("a =", a)
print("b =", b)
print("指数型衰减函数的具体形式为：f(x) =", a, "* exp(-", b, "* x)")

